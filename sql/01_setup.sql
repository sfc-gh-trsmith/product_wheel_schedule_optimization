CREATE TABLE IF NOT EXISTS PRODUCT_WHEEL_OPT.ATOMIC.DIM_CUSTOMER (
    CUSTOMER_ID      INTEGER NOT NULL PRIMARY KEY,
    CUSTOMER_CODE    VARCHAR(20) NOT NULL,
    CUSTOMER_NAME    VARCHAR(200) NOT NULL,
