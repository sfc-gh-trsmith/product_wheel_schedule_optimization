    LINE_TYPE                   VARCHAR(50),
    IS_ALLERGEN_DEDICATED_FLAG  BOOLEAN DEFAULT FALSE,
    FOREIGN KEY (PLANT_ID) REFERENCES PRODUCT_WHEEL_OPT.ATOMIC.DIM_PLANT(PLANT_ID)
);

CREATE TABLE IF NOT EXISTS PRODUCT_WHEEL_OPT.ATOMIC.DIM_FORMULATION (
    FORMULATION_ID          INTEGER NOT NULL PRIMARY KEY,
    FORMULATION_CODE        VARCHAR(50) NOT NULL,
    FORMULATION_DESCRIPTION VARCHAR(500),
    ALLERGEN_CLASS          VARCHAR(100),
