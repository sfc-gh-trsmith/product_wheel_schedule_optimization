USE ROLE SYSADMIN;

CREATE WAREHOUSE IF NOT EXISTS PRODUCT_WHEEL_SCHEDULE_OPTIMIZATION_WH
    WAREHOUSE_SIZE = 'XSMALL'
    AUTO_SUSPEND = 120
    AUTO_RESUME = TRUE;

CREATE DATABASE IF NOT EXISTS PRODUCT_WHEEL_OPT;
DROP SCHEMA IF EXISTS PRODUCT_WHEEL_OPT.PUBLIC;

CREATE SCHEMA IF NOT EXISTS PRODUCT_WHEEL_OPT.RAW;
CREATE SCHEMA IF NOT EXISTS PRODUCT_WHEEL_OPT.ATOMIC;
CREATE SCHEMA IF NOT EXISTS PRODUCT_WHEEL_OPT.DATA_MART;

USE WAREHOUSE PRODUCT_WHEEL_SCHEDULE_OPTIMIZATION_WH;

CREATE STAGE IF NOT EXISTS PRODUCT_WHEEL_OPT.RAW.DATA_STAGE
    DIRECTORY = (ENABLE = TRUE);

------------------------------------------------------------------------
-- ATOMIC Dimension Tables
------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS PRODUCT_WHEEL_OPT.ATOMIC.DIM_PLANT (
    PLANT_ID            INTEGER NOT NULL PRIMARY KEY,
    PLANT_CODE          VARCHAR(20) NOT NULL,
    PLANT_NAME          VARCHAR(100) NOT NULL,
    TIMEZONE            VARCHAR(50),
    REGION              VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS PRODUCT_WHEEL_OPT.ATOMIC.DIM_PRODUCTION_LINE (
    LINE_ID                     INTEGER NOT NULL PRIMARY KEY,
    PLANT_ID                    INTEGER NOT NULL,
    LINE_CODE                   VARCHAR(20) NOT NULL,
