    MIP_GAP_PCT             FLOAT,
    INV_COST_MULTIPLIER     FLOAT,
    BO_COST_MULTIPLIER      FLOAT,
    CO_COST_MULTIPLIER      FLOAT,
    DEMAND_MULTIPLIER       FLOAT,
    DEMAND_SHOCK_FAMILY     VARCHAR(100),
    DEMAND_SHOCK_PCT        FLOAT,
    INV_MULTIPLIER          FLOAT,
    PLANT_FILTER            VARCHAR(500),
    LINE_FILTER             VARCHAR(500),
    MAX_PRODUCTS_PER_LINE   INTEGER,
    SOLVER_ENGINE           VARCHAR(50),
    SOLVE_TIME_SEC          FLOAT,
    TOTAL_OBJECTIVE_COST    FLOAT,
    STATUS                  VARCHAR(20)
);

CREATE OR REPLACE VIEW PRODUCT_WHEEL_OPT.DATA_MART.SCENARIO_COMPARISON AS
SELECT
    k.SCENARIO_ID,
    sp.CREATED_AT,
    sp.HORIZON_DAYS,
    sp.DEMAND_MULTIPLIER,
    sp.INV_COST_MULTIPLIER,
    sp.BO_COST_MULTIPLIER,
    sp.CO_COST_MULTIPLIER,
    SUM(k.TOTAL_PLANNED_QTY) / NULLIF(SUM(k.TOTAL_DEMAND_QTY), 0) AS OVERALL_FILL_RATE,
    SUM(k.CHANGEOVER_HOURS) AS TOTAL_CHANGEOVER_HOURS,
    AVG(k.INVENTORY_DAYS_OF_SUPPLY) AS AVG_DAYS_OF_SUPPLY,
    SUM(k.OBJECTIVE_COST) AS TOTAL_OBJECTIVE_COST,
    COUNT(DISTINCT k.LINE_CODE) AS LINES_SOLVED
FROM PRODUCT_WHEEL_OPT.DATA_MART.FACT_SERVICE_AND_SCHEDULE_KPI k
LEFT JOIN PRODUCT_WHEEL_OPT.DATA_MART.SCENARIO_PARAMETERS sp ON k.SCENARIO_ID = sp.SCENARIO_ID
GROUP BY k.SCENARIO_ID, sp.CREATED_AT, sp.HORIZON_DAYS,
         sp.DEMAND_MULTIPLIER, sp.INV_COST_MULTIPLIER,
         sp.BO_COST_MULTIPLIER, sp.CO_COST_MULTIPLIER;
